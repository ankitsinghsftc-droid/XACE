//! # Mutation Transaction
//!
//! An atomic, ordered group of DSLOperations that mutate the CGS.
//! Either all operations succeed or none do — no partial commits (I8).
//!
//! ## What a Transaction Is
//! A MutationTransaction is the fundamental unit of schema change in XACE.
//! Every design change — no matter how it originated (natural language prompt,
//! manual GDE input, migration rule, or code generation) — is expressed
//! as a MutationTransaction before touching the CGS.
//!
//! ## Atomicity (I8)
//! Global Invariant I8: Schema mutations are applied atomically.
//! Partial commits are FORBIDDEN. If operation 3 of 5 fails validation,
//! the entire transaction is rolled back. The CGS returns to its exact
//! pre-transaction state. No intermediate state is ever observable.
//!
//! ## Transaction Lifecycle
//! Created (PIL/GDE) → Validated (consistency_validator) →
//! Critiqued (critique_engine) → Approved (safety_scope_guard) →
//! Applied (MutationGate) → CGS Updated → ExecutionPlan recompiled
//!
//! ## Source Tracking
//! Every transaction records where it came from — a user prompt,
//! a manual GDE operation, a migration rule, or a code generation step.
//! This is the full audit trail for every CGS change ever made.
//!
//! ## Schema Version Target
//! Each transaction declares which CGS version it was built against.
//! If the CGS has been mutated by another transaction since this one
//! was created, the version check fails and the transaction is rejected.
//! This prevents concurrent mutation conflicts.

use std::collections::BTreeMap;
use serde::{Deserialize, Serialize};
use crate::mutation::dsl_operation::DslOperation;
use crate::mutation::usmc_categories::UsmcCategory;

// ── Transaction ID ────────────────────────────────────────────────────────────

/// Unique identifier for a MutationTransaction.
/// Format: UUID v4 string assigned at transaction creation.
/// Never reused. Used for audit trail, rollback tracking, and
/// replay integrity validation.
pub type TransactionId = String;

// ── Mutation Source ───────────────────────────────────────────────────────────

/// Where this transaction originated.
///
/// Used for audit trail, Design Mentor suggestions, and NLTL translation.
/// Every transaction must declare its source — anonymous mutations
/// are rejected by the GDE.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub enum MutationSource {
    /// Generated from a natural language prompt via the PIL pipeline.
    /// `prompt_text` is the original user prompt that produced this transaction.
    UserPrompt {
        /// The original prompt text submitted by the user.
        prompt_text: String,
        /// The PIL session ID this prompt was processed in.
        session_id: String,
    },

    /// Created manually via direct GDE DSL input.
    /// Used when a technical user writes DSL operations directly.
    ManualDsl {
        /// Identifier of the user or tool that created this transaction.
        author_id: String,
    },

    /// Generated by the Schema Migration engine when loading an old save.
    /// Applied automatically during CGS version upgrade.
    Migration {
        /// The CGS version being migrated from.
        from_version: String,
        /// The CGS version being migrated to.
        to_version: String,
    },

    /// Generated by the Code Generation Engine (Phase 13).
    /// Applied when a new system implementation is confirmed by the user.
    CodeGeneration {
        /// The system ID that was generated.
        system_id: String,
    },

    /// Generated by the Game Genesis Engine (Phase 16).
    /// Applied when a new game is created from a genre template.
    Genesis {
        /// The genre template used.
        template_id: String,
    },
}

impl std::fmt::Display for MutationSource {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            MutationSource::UserPrompt { session_id, .. } =>
                write!(f, "UserPrompt(session={})", session_id),
            MutationSource::ManualDsl { author_id } =>
                write!(f, "ManualDsl(author={})", author_id),
            MutationSource::Migration { from_version, to_version } =>
                write!(f, "Migration({} → {})", from_version, to_version),
            MutationSource::CodeGeneration { system_id } =>
                write!(f, "CodeGeneration(system={})", system_id),
            MutationSource::Genesis { template_id } =>
                write!(f, "Genesis(template={})", template_id),
        }
    }
}

// ── Transaction Status ────────────────────────────────────────────────────────

/// The current status of a MutationTransaction in its lifecycle.
///
/// Transactions move forward through statuses — never backward.
/// A Committed transaction is immutable and permanently recorded.
/// A RolledBack transaction is permanently discarded.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub enum TransactionStatus {
    /// Transaction has been created and is awaiting validation.
    Pending,

    /// Transaction has passed consistency validation.
    /// Awaiting critique engine review.
    Validated,

    /// Transaction has passed critique engine review.
    /// Awaiting safety scope guard approval.
    Critiqued,

    /// Transaction has been approved by all gates.
    /// Ready for application to CGS via Mutation Gate.
    Approved,

    /// Transaction has been successfully applied to the CGS.
    /// CGS hash has been recomputed. ExecutionPlan flagged for recompile.
    /// This is a terminal success state.
    Committed,

    /// Transaction was rejected at some stage of the pipeline.
    /// `reason` carries the rejection message for user feedback.
    Rejected {
        /// Human-readable reason for rejection.
        reason: String,
    },

    /// Transaction was applied but then rolled back due to a failure.
    /// CGS has been restored to pre-transaction state exactly.
    /// This is a terminal failure state.
    RolledBack {
        /// Human-readable reason for rollback.
        reason: String,
    },
}

impl TransactionStatus {
    /// Returns true if this transaction has reached a terminal state.
    pub fn is_terminal(&self) -> bool {
        matches!(
            self,
            TransactionStatus::Committed
                | TransactionStatus::Rejected { .. }
                | TransactionStatus::RolledBack { .. }
        )
    }

    /// Returns true if this transaction was successfully committed.
    pub fn is_committed(&self) -> bool {
        matches!(self, TransactionStatus::Committed)
    }

    /// Returns true if this transaction failed for any reason.
    pub fn is_failed(&self) -> bool {
        matches!(
            self,
            TransactionStatus::Rejected { .. } | TransactionStatus::RolledBack { .. }
        )
    }
}

impl std::fmt::Display for TransactionStatus {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            TransactionStatus::Pending => write!(f, "Pending"),
            TransactionStatus::Validated => write!(f, "Validated"),
            TransactionStatus::Critiqued => write!(f, "Critiqued"),
            TransactionStatus::Approved => write!(f, "Approved"),
            TransactionStatus::Committed => write!(f, "Committed"),
            TransactionStatus::Rejected { reason } =>
                write!(f, "Rejected({})", reason),
            TransactionStatus::RolledBack { reason } =>
                write!(f, "RolledBack({})", reason),
        }
    }
}

// ── Mutation Transaction ──────────────────────────────────────────────────────

/// An atomic group of DSLOperations representing one schema change.
///
/// The fundamental unit of CGS mutation. Every change to the game
/// schema — regardless of origin — is expressed as a MutationTransaction.
///
/// ## Operation Ordering
/// Operations are applied in the order declared in `operations`.
/// The GDE's transaction_builder orders them correctly before creation.
/// The Mutation Gate applies them in this exact order — never reordered.
///
/// ## Metadata
/// `metadata` carries contextual information used by the Design Mentor,
/// NLTL translation layer, and audit trail. It is not used by the
/// Mutation Gate — only by the pipeline stages before and after commit.
///
/// ## Required Recompile
/// If any operation changes the system graph structure (adds/removes systems,
/// changes read/write declarations, changes phase assignments), the SGC
/// must recompile the ExecutionPlan. `required_recompile` signals this.
/// The runtime does not advance to the next tick until recompile is done (I7).
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct MutationTransaction {
    /// Unique identifier. UUID v4. Never reused.
    pub id: TransactionId,

    /// Ordered list of operations to apply atomically.
    /// Applied in declaration order by the Mutation Gate.
    pub operations: Vec<DslOperation>,

    /// Whether all operations must succeed for any to be applied.
    /// Always true in XACE — partial commits are forbidden (I8).
    /// Kept as a field for schema compatibility and documentation clarity.
    pub atomic: bool,

    /// The CGS version this transaction was built against.
    /// Rejected if the current CGS version does not match.
    /// Prevents concurrent mutation conflicts.
    pub schema_version_target: String,

    /// The USMC category of this transaction's primary intent.
    pub usmc_category: UsmcCategory,

    /// Where this transaction originated.
    pub source: MutationSource,

    /// Current status in the transaction lifecycle.
    pub status: TransactionStatus,

    /// Confidence score from the PIL pipeline (0.0 - 1.0).
    /// 1.0 = maximum confidence. Below threshold triggers clarification.
    /// Always 1.0 for ManualDsl and Migration sources.
    pub confidence: f32,

    /// Whether applying this transaction requires SGC recompilation.
    /// True when systems are added, removed, or their read/write sets change.
    pub required_recompile: bool,

    /// ISO 8601 timestamp when this transaction was created.
    pub created_at: String,

    /// Additional key-value metadata for audit trail and Design Mentor.
    /// BTreeMap for deterministic key ordering (D11).
    pub metadata: BTreeMap<String, String>,
}

impl MutationTransaction {
    /// Creates a new pending transaction from a user prompt.
    pub fn from_prompt(
        id: impl Into<String>,
        operations: Vec<DslOperation>,
        schema_version_target: impl Into<String>,
        usmc_category: UsmcCategory,
        prompt_text: impl Into<String>,
        session_id: impl Into<String>,
        confidence: f32,
        created_at: impl Into<String>,
    ) -> Self {
        Self {
            id: id.into(),
            operations,
            atomic: true,
            schema_version_target: schema_version_target.into(),
            usmc_category,
            source: MutationSource::UserPrompt {
                prompt_text: prompt_text.into(),
                session_id: session_id.into(),
            },
            status: TransactionStatus::Pending,
            confidence,
            required_recompile: false,
            created_at: created_at.into(),
            metadata: BTreeMap::new(),
        }
    }

    /// Creates a new pending transaction from a manual DSL operation.
    pub fn from_manual_dsl(
        id: impl Into<String>,
        operations: Vec<DslOperation>,
        schema_version_target: impl Into<String>,
        usmc_category: UsmcCategory,
        author_id: impl Into<String>,
        created_at: impl Into<String>,
    ) -> Self {
        Self {
            id: id.into(),
            operations,
            atomic: true,
            schema_version_target: schema_version_target.into(),
            usmc_category,
            source: MutationSource::ManualDsl {
                author_id: author_id.into(),
            },
            status: TransactionStatus::Pending,
            confidence: 1.0,
            required_recompile: false,
            created_at: created_at.into(),
            metadata: BTreeMap::new(),
        }
    }

    /// Creates a migration transaction for CGS version upgrades.
    pub fn from_migration(
        id: impl Into<String>,
        operations: Vec<DslOperation>,
        from_version: impl Into<String>,
        to_version: impl Into<String>,
        created_at: impl Into<String>,
    ) -> Self {
        let from = from_version.into();
        let to = to_version.into();
        Self {
            id: id.into(),
            operations,
            atomic: true,
            schema_version_target: to.clone(),
            usmc_category: UsmcCategory::Modify,
            source: MutationSource::Migration {
                from_version: from,
                to_version: to,
            },
            status: TransactionStatus::Pending,
            confidence: 1.0,
            required_recompile: false,
            created_at: created_at.into(),
            metadata: BTreeMap::new(),
        }
    }

    /// Returns true if this transaction has no operations.
    /// Empty transactions are rejected by the GDE before pipeline entry.
    pub fn is_empty(&self) -> bool {
        self.operations.is_empty()
    }

    /// Returns the number of operations in this transaction.
    pub fn operation_count(&self) -> usize {
        self.operations.len()
    }

    /// Marks this transaction as validated.
    /// Called by the consistency_validator after passing all checks.
    pub fn mark_validated(&mut self) {
        self.status = TransactionStatus::Validated;
    }

    /// Marks this transaction as critiqued.
    /// Called by the critique_engine after review.
    pub fn mark_critiqued(&mut self) {
        self.status = TransactionStatus::Critiqued;
    }

    /// Marks this transaction as approved.
    /// Called by the safety_scope_guard after all risk checks pass.
    pub fn mark_approved(&mut self) {
        self.status = TransactionStatus::Approved;
    }

    /// Marks this transaction as committed.
    /// Called by the MutationGate after successful application.
    pub fn mark_committed(&mut self) {
        self.status = TransactionStatus::Committed;
    }

    /// Marks this transaction as rejected with a reason.
    /// Called by any pipeline stage that blocks the transaction.
    pub fn mark_rejected(&mut self, reason: impl Into<String>) {
        self.status = TransactionStatus::Rejected {
            reason: reason.into(),
        };
    }

    /// Marks this transaction as rolled back with a reason.
    /// Called by the MutationGate if application fails mid-transaction.
    pub fn mark_rolled_back(&mut self, reason: impl Into<String>) {
        self.status = TransactionStatus::RolledBack {
            reason: reason.into(),
        };
    }

    /// Adds a metadata key-value pair to this transaction.
    pub fn add_metadata(
        &mut self,
        key: impl Into<String>,
        value: impl Into<String>,
    ) {
        self.metadata.insert(key.into(), value.into());
    }

    /// Returns true if this transaction requires SGC recompilation.
    pub fn needs_recompile(&self) -> bool {
        self.required_recompile
    }

    /// Flags this transaction as requiring SGC recompilation.
    /// Called by the PIL's determinism audit pass (Phase 13, Pass 4).
    pub fn flag_recompile_required(&mut self) {
        self.required_recompile = true;
    }

    /// Validates this transaction for basic structural correctness.
    ///
    /// Checks:
    /// - Transaction ID is not empty
    /// - At least one operation exists
    /// - All operations pass basic validation
    /// - Schema version target is not empty
    /// - Confidence is in range 0.0-1.0
    /// - atomic is always true (enforced)
    ///
    /// Full schema and path validation is performed by the
    /// consistency_validator in the GDE pipeline (Phase 12).
    pub fn validate(&self) -> Result<(), String> {
        if self.id.is_empty() {
            return Err("MutationTransaction ID must not be empty".into());
        }

        if self.operations.is_empty() {
            return Err(format!(
                "MutationTransaction {} has no operations — \
                 empty transactions are not allowed",
                self.id
            ));
        }

        if !self.atomic {
            return Err(format!(
                "MutationTransaction {} has atomic=false — \
                 all XACE transactions must be atomic (I8)",
                self.id
            ));
        }

        if self.schema_version_target.is_empty() {
            return Err(format!(
                "MutationTransaction {} has no schema_version_target",
                self.id
            ));
        }

        if !(0.0..=1.0).contains(&self.confidence) {
            return Err(format!(
                "MutationTransaction {} confidence {} is out of range 0.0-1.0",
                self.id, self.confidence
            ));
        }

        // Validate all operations
        for (i, op) in self.operations.iter().enumerate() {
            op.validate().map_err(|e| {
                format!(
                    "MutationTransaction {} operation {}: {}",
                    self.id, i, e
                )
            })?;
        }

        Ok(())
    }
}

// ── Tests ─────────────────────────────────────────────────────────────────────

#[cfg(test)]
mod tests {
    use super::*;
    use crate::mutation::dsl_operation::{DslValue, TypeHint};

    fn test_operation() -> DslOperation {
        DslOperation::set(
            "modes.mode_arena.actors.actor_player.stats.move_speed",
            DslValue::Float(5.0),
            TypeHint::Float,
        )
    }

    fn test_transaction() -> MutationTransaction {
        MutationTransaction::from_manual_dsl(
            "txn-001",
            vec![test_operation()],
            "0.1.0",
            UsmcCategory::Modify,
            "author_test",
            "2026-01-01T00:00:00Z",
        )
    }

    #[test]
    fn new_transaction_is_pending() {
        let txn = test_transaction();
        assert_eq!(txn.status, TransactionStatus::Pending);
    }

    #[test]
    fn new_transaction_is_atomic() {
        let txn = test_transaction();
        assert!(txn.atomic);
    }

    #[test]
    fn transaction_validates_successfully() {
        assert!(test_transaction().validate().is_ok());
    }

    #[test]
    fn empty_id_fails_validation() {
        let mut txn = test_transaction();
        txn.id = String::new();
        assert!(txn.validate().is_err());
    }

    #[test]
    fn empty_operations_fails_validation() {
        let mut txn = test_transaction();
        txn.operations.clear();
        assert!(txn.validate().is_err());
    }

    #[test]
    fn non_atomic_fails_validation() {
        let mut txn = test_transaction();
        txn.atomic = false;
        assert!(txn.validate().is_err());
    }

    #[test]
    fn confidence_out_of_range_fails() {
        let mut txn = test_transaction();
        txn.confidence = 1.5;
        assert!(txn.validate().is_err());
    }

    #[test]
    fn lifecycle_progression_works() {
        let mut txn = test_transaction();
        assert_eq!(txn.status, TransactionStatus::Pending);
        txn.mark_validated();
        assert_eq!(txn.status, TransactionStatus::Validated);
        txn.mark_critiqued();
        assert_eq!(txn.status, TransactionStatus::Critiqued);
        txn.mark_approved();
        assert_eq!(txn.status, TransactionStatus::Approved);
        txn.mark_committed();
        assert!(txn.status.is_committed());
        assert!(txn.status.is_terminal());
    }

    #[test]
    fn rejection_is_terminal_and_failed() {
        let mut txn = test_transaction();
        txn.mark_rejected("Type mismatch on field");
        assert!(txn.status.is_terminal());
        assert!(txn.status.is_failed());
        assert!(!txn.status.is_committed());
    }

    #[test]
    fn rollback_is_terminal_and_failed() {
        let mut txn = test_transaction();
        txn.mark_rolled_back("Operation 2 failed schema constraint");
        assert!(txn.status.is_terminal());
        assert!(txn.status.is_failed());
    }

    #[test]
    fn recompile_flag_works() {
        let mut txn = test_transaction();
        assert!(!txn.needs_recompile());
        txn.flag_recompile_required();
        assert!(txn.needs_recompile());
    }

    #[test]
    fn metadata_stored_correctly() {
        let mut txn = test_transaction();
        txn.add_metadata("intent", "make player faster");
        txn.add_metadata("mode", "FULLY_ASSISTED");
        assert_eq!(txn.metadata.get("intent").unwrap(), "make player faster");
        assert_eq!(txn.metadata.get("mode").unwrap(), "FULLY_ASSISTED");
    }

    #[test]
    fn operation_count_correct() {
        let txn = test_transaction();
        assert_eq!(txn.operation_count(), 1);
    }

    #[test]
    fn is_empty_false_for_nonempty() {
        let txn = test_transaction();
        assert!(!txn.is_empty());
    }

    #[test]
    fn from_prompt_sets_source_correctly() {
        let txn = MutationTransaction::from_prompt(
            "txn-002",
            vec![test_operation()],
            "0.1.0",
            UsmcCategory::Create,
            "add a health system",
            "session-abc",
            0.95,
            "2026-01-01T00:00:00Z",
        );
        assert!(matches!(
            txn.source,
            MutationSource::UserPrompt { .. }
        ));
        assert_eq!(txn.confidence, 0.95);
    }

    #[test]
    fn from_migration_sets_source_correctly() {
        let txn = MutationTransaction::from_migration(
            "txn-003",
            vec![test_operation()],
            "0.1.0",
            "0.2.0",
            "2026-01-01T00:00:00Z",
        );
        assert!(matches!(
            txn.source,
            MutationSource::Migration { .. }
        ));
        assert_eq!(txn.confidence, 1.0);
    }

    #[test]
    fn manual_dsl_confidence_is_one() {
        let txn = test_transaction();
        assert_eq!(txn.confidence, 1.0);
    }

    #[test]
    fn transaction_status_display() {
        assert_eq!(TransactionStatus::Pending.to_string(), "Pending");
        assert_eq!(TransactionStatus::Committed.to_string(), "Committed");
    }

    #[test]
    fn mutation_source_display() {
        let source = MutationSource::ManualDsl {
            author_id: "user_001".into(),
        };
        assert!(source.to_string().contains("ManualDsl"));
    }
}